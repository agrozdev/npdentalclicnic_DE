<h2>Frequently asked questions</h2>

<h3>Is the surgery painful?</h3>
<p>No. Implants are placed under local anaesthesia, which means the area is fully numb during the procedure. Most patients describe the discomfort afterwards as significantly less than a tooth extraction. Mild swelling for 1–3 days is normal and is well controlled with simple painkillers.</p>

<h3>How long does an implant last?</h3>
<p>With premium implant brands and good oral hygiene, dental implants typically last 25 years to a lifetime. The crown on top usually needs replacement once every 12–20 years — much like any high-quality crown.</p>

<h3>Can I fly home the day after surgery?</h3>
<p>For single implants and small cases, yes — most patients fly home 24–48 hours after surgery without any issues. For larger cases such as All-on-4, we usually recommend an extra night for monitoring.</p>

<h3>What if I do not have enough bone for an implant?</h3>
<p>Bone loss is common after a tooth has been missing for some time, and is rarely a deal-breaker. Modern bone grafting and sinus lift techniques can restore enough volume to place implants in the vast majority of cases. Your CBCT scan will show whether bone augmentation is needed and what it would add to the plan.</p>

