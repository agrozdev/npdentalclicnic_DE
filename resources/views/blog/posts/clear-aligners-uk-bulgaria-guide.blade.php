<h2>What are clear aligners?</h2>
<p>Clear aligners are transparent, custom-made plastic trays that fit precisely over your teeth. Each tray gently moves your teeth a fraction of a millimetre, and you change to the next tray every 1–2 weeks. Over a series of trays, your teeth move into the planned final position.</p>
<p>They differ from traditional braces in three important ways:</p>
<ul>
    <li><strong>Almost invisible</strong> — most people will not notice you are wearing them.</li>
    <li><strong>Removable</strong> — you take them out to eat, drink and clean your teeth.</li>
    <li><strong>Designed digitally from day one</strong> — you can preview your future smile before treatment starts.</li>
</ul>

<h2>What can clear aligners actually treat?</h2>
<p>Modern aligner systems are clinically proven for the vast majority of orthodontic concerns adults present with. They are particularly effective for:</p>
<ul>
    <li>Mild to moderate crowding</li>
    <li>Spacing and gaps between teeth</li>
    <li>Tipped or rotated front teeth</li>
    <li>Mild crossbite, overbite or underbite</li>
    <li>Relapse after old braces</li>
</ul>
<p>Where fixed braces still tend to outperform aligners is in severe rotations of large teeth, complex bite issues that need rigid mechanical control, or cases involving significant vertical movement. A good clinician will tell you honestly when aligners are not the best tool for the job.</p>

