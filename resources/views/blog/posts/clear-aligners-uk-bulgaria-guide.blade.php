<h2>How does aligner treatment work, step by step?</h2>

<h3>Step 1 — Free online consultation</h3>
<p>You send a few photos of your smile and describe what you want to change. The clinic tells you honestly whether aligners can deliver the result you want, and gives you an estimated price range. No payment at this stage.</p>

<h3>Step 2 — First visit to Bulgaria (1–2 days)</h3>
<p>An intra-oral 3D scan is taken — no messy putty impressions. From the scan, a specialist designs your full digital treatment plan and you can preview your final smile before any aligner is made.</p>

<h3>Step 3 — Aligners delivered</h3>
<p>Your full set of aligners is manufactured and posted to you in the UK. You change to a new tray every 1–2 weeks and wear each one for 20–22 hours a day.</p>

<h3>Step 4 — Remote monitoring</h3>
<p>You send progress photos every few weeks and we keep you on track by WhatsApp and email. Most patients only need to come back to Bulgaria once or twice during the active phase.</p>

<h3>Step 5 — Refinements and retainers</h3>
<p>At the end of treatment, a small set of refinement aligners may be issued to perfect the result. You then move to retainers (worn at night) to keep your new smile in place permanently.</p>

<h2>What does it feel like to wear aligners?</h2>
<p>The first 24–48 hours with each new aligner can feel tight as your teeth start to move — most patients describe it as pressure rather than pain. After that, the aligners feel like a natural part of your day. There are no metal brackets to irritate your cheeks, no food restrictions and no special cleaning routines.</p>
<p>For aligners to work, they must be worn 20–22 hours a day. They only come out for eating, drinking anything other than water, and brushing. If you know yourself well enough to commit to that, the experience is excellent. If you would prefer a “fit and forget” option, fixed braces remove the discipline question.</p>

<h2>Are clear aligners as effective as braces?</h2>
<p>For mild and moderate cases — which is the majority of adult orthodontic concerns — yes, aligners deliver results that are clinically equivalent to fixed braces. For severe and complex cases, fixed braces remain the more reliable tool. The treatment time is broadly similar between the two — aligners are not faster, despite some marketing claims.</p>
<p>This is why a good clinician will sometimes recommend braces, even when a patient walks in asking specifically for aligners. Honesty about what each tool does best is the difference between a good outcome and a relapse.</p>

