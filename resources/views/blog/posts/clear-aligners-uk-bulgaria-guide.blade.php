<h2>Frequently asked questions</h2>

<h3>Will clear aligners affect my speech?</h3>
<p>You may notice a very slight lisp on certain sounds (especially "s" and "th") for the first few days of wearing aligners. The brain adapts within roughly a week and most patients report no speech impact thereafter.</p>

<h3>Can I drink coffee or tea with aligners in?</h3>
<p>You should remove the aligners for anything other than plain water. Hot drinks can warp the plastic, and pigmented drinks (coffee, tea, red wine) will stain the aligners and the tooth surfaces beneath them. Take them out, drink, brush, put them back in.</p>

<h3>Do aligners give the same results as Invisalign?</h3>
<p>Modern aligner systems use the same medical-grade thermoplastic and the same biomechanical principles as Invisalign. The result depends on the planner, not the brand label. Many clinics outside Invisalign’s network use equally capable systems at lower cost — which is one reason aligner treatment in Bulgaria is significantly more affordable.</p>

<h3>What happens if I lose an aligner?</h3>
<p>Tell your clinic straight away. The standard guidance is to drop back to your previous aligner so you do not lose progress, while a replacement is manufactured and posted to you.</p>

<h3>Will I need to wear retainers forever?</h3>
<p>Yes — though only at night. Teeth have memory and will gradually drift back towards their original positions without retention. Wearing a thin clear retainer at night is the standard, simple way to keep your new smile in place for life.</p>

